<p>
    Protoshift is an addicting, fast paced, twitch based, arcade game filled
    with challenge, inspired by Super Hexagon. Put your mouse hand to the test
    while attempting to complete impossible levels and compete on global
    leader-boards.
</p>

<p>
    Protoshift was originally released on Steam on Jan 15, 2016. A few years
    after release I sold the Steam version to a publisher, who's account was
    taken down some time later.
</p>

